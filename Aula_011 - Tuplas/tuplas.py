#Tuplas são similares a listas e dicionarios, o que difere é que sao feitas com parenteses() e seus valores são imutaveis.

#Formas de criar uma tupla
tup1 = ('physics', 'chemistry', 1997, 2000)
tup2 = (1, 2, 3, 4, 5)
tup3 = "a", "b", "c", "d", "e", "f"

#tuplas podem ser vazias tambem.
tup4 = ()

print(tup4)
#Não é possivel alterar o valor de uma tupla, mas adicionar elementos nela é.
tup4 = (50,)
print(tup4)