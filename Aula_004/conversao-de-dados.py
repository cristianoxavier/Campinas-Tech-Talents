#Definimos a variavel inicial, do tipo string.
numero = "1"
print(type(numero))

#mudamos o tipo da variavel para uma variavel do tipo Int.
numero = int(numero)
print(type(numero))

#Mudamos novamente o tipo da variavel para o tipo float.

numero = float(numero)
print(type(numero))

#Voltamos a variavel para o tipo inicial, que era uma string.

numero = str(numero)
print(type(numero))