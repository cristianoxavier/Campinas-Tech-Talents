a = 10.0
b = 20.0

print(a + b)  #soma
print(a - b)  #subtração
print(a * b)  #multiplicação
print(a / b)  #divisão
print(a % b)  #módulo ou resto
print(a ** b) #potenciação
print(a // b) #o // faz a divisão e retorna o resultado em inteiro arredondando para baixo, resultado em float se o número for float
